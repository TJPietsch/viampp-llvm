#ifndef VIAMPPSUBTARGET_H
#define VIAMPPSUBTARGET_H

#include "VIAMPPFrameLowering.h"
#include "VIAMPPISelLowering.h"
#include "VIAMPPInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "VIAMPPGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class VIAMPPSubtarget : public VIAMPPGenSubtargetInfo {
  virtual void anchor();

  const VIAMPPInstrInfo InstrInfo;
  const VIAMPPFrameLowering FrameLowering;
  const VIAMPPTargetLowering TLInfo;
  const SelectionDAGTargetInfo TSInfo;

public:
  // Initializes the data members to match that of the specified triple.
  VIAMPPSubtarget(const Triple &TT, const StringRef CPU, const StringRef FS, const VIAMPPTargetMachine &TM);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const VIAMPPInstrInfo *getInstrInfo() const override {
  	return &InstrInfo;
  }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const VIAMPPTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
      return &TSInfo;
  }

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);
};
} // End llvm namespace

#endif
